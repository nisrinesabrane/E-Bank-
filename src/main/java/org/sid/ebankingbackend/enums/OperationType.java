package org.sid.ebankingbackend.enums;

/**
 * Enum representing the type of operations that can be performed on a bank account.
 * 
 * - DEBIT: Represents an operation that subtracts money from the account.
 * - CREDIT: Represents an operation that adds money to the account.
 */
public enum OperationType {
    DEBIT,  // Subtract money from the account
    CREDIT  // Add money to the account
}
